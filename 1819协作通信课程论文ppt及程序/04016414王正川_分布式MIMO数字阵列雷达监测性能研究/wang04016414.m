a=0.1;f=0.0625;
n=[0:15];
xb=exp(-a*n).*sin(2*pi*f*n);
f=fft(xb);
subplot(3,2,1);
stem(n,xb);
title('(a)');
subplot(3,2,2);
stem(n,abs(f));
title('(b)');
